def generateTablefromCSV():
    table=[]
    infile = open('table.csv','r',encoding='UTF8')
    while True:
         line = infile.readline()
         if not line: break
         #line = line.replace(",,",",-,-").replace(",\n",",-").strip()
         line = line.replace("\n","").split(",")
         table.append(line)
    
    infile.close()
    table[0][0]='0'
    

    for i in table:
        print(i,end=",\\\n")

